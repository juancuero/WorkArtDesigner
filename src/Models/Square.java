package Models;

import java.awt.Color;

/**
 *
 * @author juanc
 */
public class Square extends Shape {
    int side;

    public Square(int side, int x, int y, Color color) {
        super(x, y, color);
        this.side = side;
    }

    public int getSide() {
        return side;
    }

    public void setSide(int side) {
        this.side = side;
    }
    
}
